function func = problem10(t1,t2,t3)

R=0.25*[0;1]*[0 1] + 0.1*[1;0]*[1 0] + 0.1*[2;0]*[2 0] + 0.1*[-1;-1]*[-1 -1] + 0.1*[0;-2.5]*[0 -2.5] + 0.1*[1.5;-1.5]*[1.5 -1.5];

I=inv(R);


H=0.25*t1*[0 0] + 0.25*t1*[0 1] + 0.1*t2*[1 0] + 0.1*t2*[2 0] + 0.1*t3*[-1 -1] + 0.1*t3*[0 -2.5] + 0.1*t3*[1.5 -1.5];

x1=[0 0];
y1=[0 1];

x2=[1 2];
y2=[0 0];

x3=[-1 0 1.5];
y3=[-1 -2.5 -1.5];

F=I*H;


x = linspace(-2,2,500);

z1 = (-(F(1,1)/F(1,2)))*x
z2 =(-(F(2,1)/F(2,2)))*x


plot(x1,y1,'m*',x2,y2,'g*',x3,y3,'r*');

hold on;

plot(x,z1);

plot(x,z2);

hold off;

disp(F);


